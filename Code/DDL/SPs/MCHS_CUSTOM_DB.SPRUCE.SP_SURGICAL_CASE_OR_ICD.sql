$$
var sql_tmp = `CREATE TEMPORARY TABLE MCHS_CUSTOM_DB.SPRUCE.TEMP_SURGICAL_CASE_OR_ICD AS (
               SELECT ICD.NCHS_ONLY_PERSON_ID, ICD.NCHS_ONLY_MRN, ICD.NCHS_ONLY_ENCOUNTER_ID, ICD.NCHS_ONLY_FIN, 
