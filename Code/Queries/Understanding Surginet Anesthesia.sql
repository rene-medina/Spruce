  ON DTA.TASK_ASSAY_CD = SRP.TASK_ASSAY_CD 
JOIN MCHS_DB.MCHS_PROD.CLINICAL_EVENT            CE
  ON CE.EVENT_ID = SPV.EVENT_ID
JOIN MCHS_DB.MCHS_PROD.CODE_VALUE                CV
  ON CV.CODE_VALUE = CE.RESULT_UNITS_CD 
