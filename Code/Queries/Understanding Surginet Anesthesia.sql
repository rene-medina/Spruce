              ,SRM.SA_REF_MEDICATION_ID
              ,MOIM.DESCRIPTION
      ORDER BY MOIM.DESCRIPTION) MEDS
  ON MEDS.SA_REF_MEDICATION_ID = SM.SA_REF_MEDICATION_ID  
